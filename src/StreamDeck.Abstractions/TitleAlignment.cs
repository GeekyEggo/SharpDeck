namespace StreamDeck
{
    /// <summary>
    /// Defines the possible title-alignment that an action's title can be.
    /// </summary>
    public static class TitleAlignment
    {
        /// <summary>
        /// Title text aligned top.
        /// </summary>
        public const string Top = "top";

        /// <summary>
        /// Title text aligned middle.
        /// </summary>
        public const string Middle = "middle";

        /// <summary>
        /// Title text aligned bottom.
        /// </summary>
        public const string Bottom = "bottom";
    }
}
