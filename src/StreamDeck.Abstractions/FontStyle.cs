namespace StreamDeck
{
    /// <summary>
    /// Defines the possible font-style that an action's title can be.
    /// </summary>
    public static class FontStyle
    {
        /// <summary>
        /// The regular font-style.
        /// </summary>
        public const string Regular = nameof(Regular);

        /// <summary>
        /// The bold font-style.
        /// </summary>
        public const string Bold = nameof(Bold);

        /// <summary>
        /// The bold-italic font-style.
        /// </summary>
        public const string BoldItalic = "Bold Italic";

        /// <summary>
        /// The italic font-style.
        /// </summary>
        public const string Italic = nameof(Italic);
    }
}
