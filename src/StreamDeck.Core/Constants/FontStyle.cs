namespace StreamDeck.Constants
{
    /// <summary>
    /// Defines the font styles supported by the Stream Deck.
    /// </summary>
    public static class FontStyle
    {
        /// <summary>
        /// Regular font.
        /// </summary>
        public const string Regular = nameof(Regular);

        /// <summary>
        /// Bold font.
        /// </summary>
        public const string Bold = nameof(Bold);

        /// <summary>
        /// Italic font.
        /// </summary>
        public const string Italic = nameof(Italic);

        /// <summary>
        /// Bold and italic font.
        /// </summary>
        public const string BoldItalic = "Bold Italic";
    }
}
