namespace SharpDeck.Layouts
{
    /// <summary>
    /// An enumeration that represents the design of a bar.
    /// </summary>
    public enum BarSubType
    {
        /// <summary>
        /// The rectangle design.
        /// </summary>
        Rectangle = 0,

        /// <summary>
        /// The double-rectangle design.
        /// </summary>
        DoubleRectangle = 1,

        /// <summary>
        /// The trapezoid design.
        /// </summary>
        Trapezoid = 2,

        /// <summary>
        /// The double-trapezoid design.
        /// </summary>
        DoubleTrapezoid = 3,

        /// <summary>
        /// The recommended design.
        /// </summary>
        Groove = 4
    }
}
