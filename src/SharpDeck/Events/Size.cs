namespace SharpDeck.Events
{
    /// <summary>
    /// Provides a size as represented by <see cref="Size.Columns"/> and <see cref="Size.Rows"/>.
    /// </summary>
    public class Size
    {
        /// <summary>
        /// Gets or sets the columns.
        /// </summary>
        public int Columns { get; set; }

        /// <summary>
        /// Gets or sets the rows.
        /// </summary>
        public int Rows { get; set; }
    }
}
